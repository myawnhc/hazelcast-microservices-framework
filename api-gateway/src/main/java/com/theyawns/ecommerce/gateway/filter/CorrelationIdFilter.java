package com.theyawns.ecommerce.gateway.filter;

import java.util.UUID;

import org.springframework.cloud.gateway.filter.GatewayFilterChain;
import org.springframework.cloud.gateway.filter.GlobalFilter;
import org.springframework.core.Ordered;
import org.springframework.http.server.reactive.ServerHttpRequest;
import org.springframework.stereotype.Component;
import org.springframework.web.server.ServerWebExchange;
import reactor.core.publisher.Mono;

/**
 * Global filter that ensures every request has a correlation ID.
 *
 * <p>If the incoming request contains an {@code X-Correlation-ID} header, it is
 * propagated as-is to downstream services and back to the client. If missing,
 * a new UUID is generated. The correlation ID is also stored as an exchange
 * attribute so other filters (logging, timing) can reference it.
 *
 * <p>Filter order: {@code -4} (runs first among custom filters).
 *
 * @author Generated by Claude Code
 * @since 1.0
 */
@Component
public class CorrelationIdFilter implements GlobalFilter, Ordered {

    /** Header name used for correlation ID propagation. */
    public static final String CORRELATION_ID_HEADER = "X-Correlation-ID";

    /** Exchange attribute key for accessing correlation ID from other filters. */
    public static final String CORRELATION_ID_ATTR = "correlationId";

    @Override
    public Mono<Void> filter(final ServerWebExchange exchange, final GatewayFilterChain chain) {
        String correlationId = exchange.getRequest().getHeaders().getFirst(CORRELATION_ID_HEADER);

        if (correlationId == null || correlationId.isBlank()) {
            correlationId = UUID.randomUUID().toString();
        }

        // Add to request going downstream
        final ServerHttpRequest mutatedRequest = exchange.getRequest().mutate()
                .header(CORRELATION_ID_HEADER, correlationId)
                .build();

        final ServerWebExchange mutatedExchange = exchange.mutate()
                .request(mutatedRequest)
                .build();

        // Store in exchange attributes for other filters
        mutatedExchange.getAttributes().put(CORRELATION_ID_ATTR, correlationId);

        // Add to response going back to client
        mutatedExchange.getResponse().getHeaders().set(CORRELATION_ID_HEADER, correlationId);

        return chain.filter(mutatedExchange);
    }

    @Override
    public int getOrder() {
        return -4;
    }
}
