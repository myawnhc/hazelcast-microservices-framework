package com.theyawns.ecommerce.inventory.config;

import org.springframework.boot.context.properties.ConfigurationProperties;

/**
 * Configuration properties for automatic stock replenishment.
 *
 * <p>When enabled, a background monitor checks product stock levels periodically
 * and triggers automatic reorders when available quantity drops below the
 * {@code reorderPoint}. This prevents stock exhaustion during sustained load
 * testing and models real-world reorder behavior.
 *
 * <p>Configuration prefix: {@code framework.inventory.replenishment}
 *
 * <p>Example configuration:
 * <pre>
 * framework:
 *   inventory:
 *     replenishment:
 *       enabled: true
 *       reorder-point: 100
 *       reorder-quantity: 1000
 *       check-interval-ms: 10000
 * </pre>
 *
 * @author Generated by Claude Code
 * @since 3.1
 */
@ConfigurationProperties(prefix = "framework.inventory.replenishment")
public class StockReplenishmentProperties {

    /**
     * Whether automatic stock replenishment is enabled (default: true).
     */
    private boolean enabled = true;

    /**
     * Stock level threshold that triggers a reorder (default: 100).
     * When a product's available quantity drops below this value,
     * replenishment is triggered.
     */
    private int reorderPoint = 100;

    /**
     * Quantity to add when replenishing (default: 1000).
     */
    private int reorderQuantity = 1000;

    /**
     * How often to check stock levels, in milliseconds (default: 10000).
     */
    private long checkIntervalMs = 10000;

    public boolean isEnabled() {
        return enabled;
    }

    public void setEnabled(boolean enabled) {
        this.enabled = enabled;
    }

    public int getReorderPoint() {
        return reorderPoint;
    }

    public void setReorderPoint(int reorderPoint) {
        this.reorderPoint = reorderPoint;
    }

    public int getReorderQuantity() {
        return reorderQuantity;
    }

    public void setReorderQuantity(int reorderQuantity) {
        this.reorderQuantity = reorderQuantity;
    }

    public long getCheckIntervalMs() {
        return checkIntervalMs;
    }

    public void setCheckIntervalMs(long checkIntervalMs) {
        this.checkIntervalMs = checkIntervalMs;
    }
}
