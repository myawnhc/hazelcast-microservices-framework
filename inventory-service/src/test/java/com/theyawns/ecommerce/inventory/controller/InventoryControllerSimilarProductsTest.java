package com.theyawns.ecommerce.inventory.controller;

import com.theyawns.ecommerce.common.domain.Product;
import com.theyawns.ecommerce.inventory.exception.GlobalExceptionHandler;
import com.theyawns.ecommerce.inventory.service.ProductService;
import com.theyawns.framework.vectorstore.SimilarityResult;
import com.theyawns.framework.vectorstore.VectorStoreService;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Nested;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.Mock;
import org.mockito.junit.jupiter.MockitoExtension;
import org.springframework.test.web.servlet.MockMvc;
import org.springframework.test.web.servlet.setup.MockMvcBuilders;

import java.math.BigDecimal;
import java.util.List;
import java.util.Map;
import java.util.Optional;

import static org.mockito.Mockito.when;
import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.get;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.jsonPath;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;

/**
 * Tests for the similar products endpoint in {@link InventoryController}.
 *
 * @author Generated by Claude Code
 * @since 2.0
 */
@ExtendWith(MockitoExtension.class)
@DisplayName("InventoryController - Similar Products Endpoint")
class InventoryControllerSimilarProductsTest {

    private MockMvc mockMvc;

    @Mock
    private ProductService productService;

    @Mock
    private VectorStoreService vectorStoreService;

    @BeforeEach
    void setUp() {
        InventoryController controller = new InventoryController(productService, vectorStoreService);
        mockMvc = MockMvcBuilders.standaloneSetup(controller)
                .setControllerAdvice(new GlobalExceptionHandler())
                .build();
    }

    @Nested
    @DisplayName("GET /api/products/{id}/similar - Vector store available")
    class VectorStoreAvailableTests {

        @Test
        @DisplayName("should return similar products when vector store is available")
        void shouldReturnSimilarProducts() throws Exception {
            when(productService.productExists("prod-1")).thenReturn(true);
            when(vectorStoreService.isAvailable()).thenReturn(true);
            when(vectorStoreService.getImplementationType())
                    .thenReturn("IMap-Based Cosine Similarity (Enterprise)");

            Product similar1 = new Product("prod-2", "SKU-002", "Similar Widget",
                    new BigDecimal("19.99"), 50);
            Product similar2 = new Product("prod-3", "SKU-003", "Another Widget",
                    new BigDecimal("24.99"), 30);

            when(vectorStoreService.findSimilarById("prod-1", 5)).thenReturn(List.of(
                    new SimilarityResult("prod-2", 0.95f, Map.of("name", "Similar Widget")),
                    new SimilarityResult("prod-3", 0.80f, Map.of("name", "Another Widget"))
            ));
            when(productService.getProduct("prod-2")).thenReturn(Optional.of(similar1));
            when(productService.getProduct("prod-3")).thenReturn(Optional.of(similar2));

            mockMvc.perform(get("/api/products/prod-1/similar"))
                    .andExpect(status().isOk())
                    .andExpect(jsonPath("$.productId").value("prod-1"))
                    .andExpect(jsonPath("$.vectorStoreAvailable").value(true))
                    .andExpect(jsonPath("$.implementation").value("IMap-Based Cosine Similarity (Enterprise)"))
                    .andExpect(jsonPath("$.similarProducts").isArray())
                    .andExpect(jsonPath("$.similarProducts.length()").value(2))
                    .andExpect(jsonPath("$.similarProducts[0].productId").value("prod-2"))
                    .andExpect(jsonPath("$.similarProducts[1].productId").value("prod-3"));
        }

        @Test
        @DisplayName("should respect custom limit parameter")
        void shouldRespectCustomLimit() throws Exception {
            when(productService.productExists("prod-1")).thenReturn(true);
            when(vectorStoreService.isAvailable()).thenReturn(true);
            when(vectorStoreService.getImplementationType()).thenReturn("Enterprise");

            Product similar1 = new Product("prod-2", "SKU-002", "Widget A",
                    new BigDecimal("19.99"), 50);

            when(vectorStoreService.findSimilarById("prod-1", 1)).thenReturn(List.of(
                    new SimilarityResult("prod-2", 0.95f, Map.of())
            ));
            when(productService.getProduct("prod-2")).thenReturn(Optional.of(similar1));

            mockMvc.perform(get("/api/products/prod-1/similar")
                            .param("limit", "1"))
                    .andExpect(status().isOk())
                    .andExpect(jsonPath("$.similarProducts.length()").value(1));
        }
    }

    @Nested
    @DisplayName("GET /api/products/{id}/similar - Vector store unavailable (Community)")
    class VectorStoreUnavailableTests {

        @Test
        @DisplayName("should return empty results with informational message")
        void shouldReturnEmptyWithMessage() throws Exception {
            when(productService.productExists("prod-1")).thenReturn(true);
            when(vectorStoreService.isAvailable()).thenReturn(false);
            when(vectorStoreService.getImplementationType()).thenReturn("No-Op (Community Edition)");

            mockMvc.perform(get("/api/products/prod-1/similar"))
                    .andExpect(status().isOk())
                    .andExpect(jsonPath("$.productId").value("prod-1"))
                    .andExpect(jsonPath("$.vectorStoreAvailable").value(false))
                    .andExpect(jsonPath("$.implementation").value("No-Op (Community Edition)"))
                    .andExpect(jsonPath("$.message").value(
                            org.hamcrest.Matchers.containsString("Enterprise Edition")))
                    .andExpect(jsonPath("$.similarProducts").isArray())
                    .andExpect(jsonPath("$.similarProducts").isEmpty());
        }
    }

    @Nested
    @DisplayName("GET /api/products/{id}/similar - Error cases")
    class ErrorCaseTests {

        @Test
        @DisplayName("should return 404 when product not found")
        void shouldReturn404WhenProductNotFound() throws Exception {
            when(productService.productExists("nonexistent")).thenReturn(false);

            mockMvc.perform(get("/api/products/nonexistent/similar"))
                    .andExpect(status().isNotFound());
        }
    }
}
