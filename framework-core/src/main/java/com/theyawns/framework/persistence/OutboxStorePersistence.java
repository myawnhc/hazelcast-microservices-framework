package com.theyawns.framework.persistence;

import java.util.List;
import java.util.Optional;

/**
 * Provider-agnostic interface for persisting outbox entries to durable storage.
 *
 * <p>Implementations of this interface are responsible for storing and loading
 * {@link PersistableOutboxEntry} records. The framework's MapStore adapter delegates
 * to this interface, keeping Hazelcast concerns separate from database concerns.
 *
 * <p>Outbox persistence uses upsert semantics — entries transition through
 * PENDING, CLAIMED, DELIVERED, and FAILED statuses, and only the latest state
 * for a given {@code eventId} matters.
 *
 * <p>Implementations must be thread-safe — Hazelcast's write-behind mechanism
 * may call {@link #persistBatch(List)} from multiple threads concurrently.
 *
 * @author Generated by Claude Code
 * @since 3.0
 * @see PersistableOutboxEntry
 * @see com.theyawns.framework.persistence.mapstore.OutboxMapStore
 */
public interface OutboxStorePersistence {

    /**
     * Persists a single outbox entry to durable storage with upsert semantics.
     *
     * @param entry the outbox entry to persist
     */
    void persist(PersistableOutboxEntry entry);

    /**
     * Persists a batch of outbox entries to durable storage with upsert semantics.
     *
     * <p>Implementations should use batch upsert for efficiency.
     *
     * @param entries the outbox entries to persist
     */
    void persistBatch(List<PersistableOutboxEntry> entries);

    /**
     * Loads a single outbox entry from durable storage.
     *
     * @param eventId the outbox entry's event ID (IMap key)
     * @return the entry if found, or empty
     */
    Optional<PersistableOutboxEntry> load(String eventId);

    /**
     * Loads all event IDs for non-delivered outbox entries.
     *
     * <p>Returns keys for entries with status PENDING, CLAIMED, or FAILED.
     * DELIVERED entries are excluded to keep the initial load small.
     * This is used by the MapLoader for EAGER initial load on restart,
     * ensuring undelivered entries are recovered.
     *
     * @return event IDs of non-delivered entries
     */
    Iterable<String> loadNonDeliveredKeys();

    /**
     * Deletes a single outbox entry from durable storage.
     *
     * @param eventId the outbox entry's event ID
     */
    void delete(String eventId);

    /**
     * Checks whether the persistence backend is available and healthy.
     *
     * @return true if the backend is reachable and ready
     */
    boolean isAvailable();
}
