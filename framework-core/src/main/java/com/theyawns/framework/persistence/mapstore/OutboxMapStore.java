package com.theyawns.framework.persistence.mapstore;

import com.hazelcast.map.MapLoaderLifecycleSupport;
import com.hazelcast.map.MapStore;
import com.hazelcast.nio.serialization.genericrecord.GenericRecord;
import com.theyawns.framework.persistence.OutboxStorePersistence;
import com.theyawns.framework.persistence.PersistableOutboxEntry;
import com.theyawns.framework.persistence.PersistenceMetrics;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import java.time.Instant;
import java.util.ArrayList;
import java.util.Collection;
import java.util.HashMap;
import java.util.List;
import java.util.Map;
import java.util.Properties;

/**
 * Hazelcast MapStore adapter that bridges the outbox IMap to an
 * {@link OutboxStorePersistence} provider.
 *
 * <p>This adapter is configured on the {@code framework_OUTBOX} IMap using
 * write-behind mode with coalescing enabled (only the latest status for each
 * entry is persisted). On cold start, {@link #loadAllKeys()} returns
 * non-delivered entry keys so Hazelcast can recover pending entries.
 *
 * <p>The nested {@code eventRecord} GenericRecord is serialized to JSON via
 * {@link GenericRecordJsonConverter} for database storage.
 *
 * @author Generated by Claude Code
 * @since 3.0
 * @see OutboxStorePersistence
 */
public class OutboxMapStore
        implements MapStore<String, GenericRecord>,
                   MapLoaderLifecycleSupport {

    private static final Logger logger = LoggerFactory.getLogger(OutboxMapStore.class);

    private final OutboxStorePersistence persistence;
    private final PersistenceMetrics metrics;
    private String mapName;

    /**
     * Creates a new OutboxMapStore without metrics.
     *
     * @param persistence the persistence provider
     */
    public OutboxMapStore(OutboxStorePersistence persistence) {
        this(persistence, null);
    }

    /**
     * Creates a new OutboxMapStore with optional metrics.
     *
     * @param persistence the persistence provider
     * @param metrics the metrics collector (may be null)
     */
    public OutboxMapStore(OutboxStorePersistence persistence, PersistenceMetrics metrics) {
        if (persistence == null) {
            throw new IllegalArgumentException("persistence cannot be null");
        }
        this.persistence = persistence;
        this.metrics = metrics;
    }

    @Override
    public void init(com.hazelcast.core.HazelcastInstance hazelcastInstance,
                     Properties properties, String mapName) {
        this.mapName = mapName;
        logger.info("OutboxMapStore initialized for map: {}", mapName);
    }

    @Override
    public void destroy() {
        logger.info("OutboxMapStore destroyed for map: {}", mapName);
    }

    // ========================================================================
    // MapStore (write) operations
    // ========================================================================

    @Override
    public void store(String key, GenericRecord value) {
        Instant start = metrics != null ? Instant.now() : null;
        PersistableOutboxEntry entry = toPersistableEntry(value);
        persistence.persist(entry);
        if (metrics != null) {
            metrics.recordStore(mapName, "outbox", start);
        }
    }

    @Override
    public void storeAll(Map<String, GenericRecord> map) {
        if (map.isEmpty()) {
            return;
        }
        Instant start = metrics != null ? Instant.now() : null;
        List<PersistableOutboxEntry> entries = new ArrayList<>(map.size());
        for (Map.Entry<String, GenericRecord> e : map.entrySet()) {
            entries.add(toPersistableEntry(e.getValue()));
        }
        persistence.persistBatch(entries);
        if (metrics != null) {
            metrics.recordBatchStore(mapName, "outbox", map.size(), start);
        }
        logger.debug("Persisted batch of {} outbox entries for map {}", map.size(), mapName);
    }

    @Override
    public void delete(String key) {
        persistence.delete(key);
        if (metrics != null) {
            metrics.recordDelete(mapName);
        }
    }

    @Override
    public void deleteAll(Collection<String> keys) {
        for (String key : keys) {
            persistence.delete(key);
        }
    }

    // ========================================================================
    // MapLoader (read) operations
    // ========================================================================

    @Override
    public GenericRecord load(String key) {
        Instant start = metrics != null ? Instant.now() : null;
        GenericRecord result = persistence.load(key)
                .map(OutboxMapStore::toGenericRecord)
                .orElse(null);
        if (metrics != null) {
            metrics.recordLoad(mapName, start);
            if (result == null) {
                metrics.recordLoadMiss(mapName);
            }
        }
        return result;
    }

    @Override
    public Map<String, GenericRecord> loadAll(Collection<String> keys) {
        Map<String, GenericRecord> result = new HashMap<>(keys.size());
        for (String key : keys) {
            GenericRecord record = load(key);
            if (record != null) {
                result.put(key, record);
            }
        }
        return result;
    }

    @Override
    public Iterable<String> loadAllKeys() {
        // Outbox uses EAGER initial load — return non-delivered entry keys
        // so Hazelcast can recover PENDING/CLAIMED/FAILED entries on restart.
        return persistence.loadNonDeliveredKeys();
    }

    // ========================================================================
    // Conversion helpers
    // ========================================================================

    /**
     * Converts a GenericRecord (from IMap) to a PersistableOutboxEntry.
     *
     * <p>The nested {@code eventRecord} GenericRecord is serialized to JSON
     * using {@link GenericRecordJsonConverter}.
     *
     * @param record the GenericRecord with OutboxEntry schema
     * @return the persistable representation
     */
    static PersistableOutboxEntry toPersistableEntry(GenericRecord record) {
        // Serialize the nested eventRecord to JSON
        GenericRecord eventRecord = record.getGenericRecord("eventRecord");
        String eventData = eventRecord != null
                ? GenericRecordJsonConverter.toJson(eventRecord)
                : "{}";

        Long lastAttemptAt = record.getNullableInt64("lastAttemptAt");
        Long claimedAt = record.getNullableInt64("claimedAt");

        return new PersistableOutboxEntry(
                record.getString("eventId"),
                record.getString("eventType"),
                eventData,
                record.getInt32("retryCount"),
                record.getString("status"),
                record.getInt64("createdAt"),
                lastAttemptAt,
                record.getString("failureReason"),
                record.getString("claimantId"),
                claimedAt
        );
    }

    /**
     * Converts a PersistableOutboxEntry back to a GenericRecord.
     *
     * <p>The JSON {@code eventData} is deserialized back to a nested
     * GenericRecord using {@link GenericRecordJsonConverter}.
     *
     * @param entry the persistable outbox entry
     * @return the reconstructed GenericRecord
     */
    static GenericRecord toGenericRecord(PersistableOutboxEntry entry) {
        GenericRecord eventRecord = GenericRecordJsonConverter.fromJson(entry.eventData());

        return com.hazelcast.nio.serialization.genericrecord.GenericRecordBuilder
                .compact("OutboxEntry")
                .setString("eventId", entry.eventId())
                .setString("eventType", entry.eventType())
                .setGenericRecord("eventRecord", eventRecord)
                .setInt32("retryCount", entry.retryCount())
                .setString("status", entry.status())
                .setInt64("createdAt", entry.createdAtMillis())
                .setNullableInt64("lastAttemptAt", entry.lastAttemptAtMillis())
                .setString("failureReason", entry.failureReason())
                .setString("claimantId", entry.claimantId())
                .setNullableInt64("claimedAt", entry.claimedAtMillis())
                .build();
    }
}
