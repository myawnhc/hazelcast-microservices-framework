package com.theyawns.framework.outbox;

import com.hazelcast.map.EntryProcessor;
import com.hazelcast.nio.serialization.genericrecord.GenericRecord;
import com.hazelcast.nio.serialization.genericrecord.GenericRecordBuilder;

import java.io.Serializable;
import java.time.Instant;
import java.util.Map;

/**
 * Hazelcast {@link EntryProcessor} that atomically transitions an outbox entry
 * from {@code PENDING} to {@code CLAIMED} status.
 *
 * <p>Executes on the partition owner with no data movement, providing compare-and-swap
 * semantics. Only one cluster member can win the CAS for a given entry â€” preventing
 * duplicate delivery when multiple pods poll the outbox simultaneously.
 *
 * <p>Returns {@code true} if the entry was successfully claimed, {@code false} if
 * it was already claimed or in another non-PENDING status.
 *
 * @author Generated by Claude Code
 * @since 3.1
 */
public class ClaimEntryProcessor implements EntryProcessor<String, GenericRecord, Boolean>, Serializable {

    private static final long serialVersionUID = 1L;

    private final String claimantId;
    private final long claimedAtMillis;

    /**
     * Creates a new ClaimEntryProcessor.
     *
     * @param claimantId the UUID of the claiming cluster member
     */
    public ClaimEntryProcessor(final String claimantId) {
        this.claimantId = claimantId;
        this.claimedAtMillis = Instant.now().toEpochMilli();
    }

    @Override
    public Boolean process(final Map.Entry<String, GenericRecord> entry) {
        GenericRecord record = entry.getValue();
        if (record == null) {
            return false;
        }

        String status = record.getString("status");
        if (!"PENDING".equals(status)) {
            return false; // Already claimed, delivered, or failed
        }

        // Rebuild the record with CLAIMED status and claim metadata
        GenericRecordBuilder builder = record.newBuilderWithClone();
        builder.setString("status", OutboxEntry.Status.CLAIMED.name());
        builder.setString("claimantId", claimantId);
        builder.setNullableInt64("claimedAt", claimedAtMillis);

        entry.setValue(builder.build());
        return true;
    }
}
