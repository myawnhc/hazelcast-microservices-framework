package com.theyawns.framework.outbox;

import com.hazelcast.map.EntryProcessor;
import com.hazelcast.nio.serialization.genericrecord.GenericRecord;
import com.hazelcast.nio.serialization.genericrecord.GenericRecordBuilder;

import java.io.Serializable;
import java.util.Map;

/**
 * Hazelcast {@link EntryProcessor} that reverts a CLAIMED outbox entry back to
 * PENDING if the claim is older than the specified timeout.
 *
 * <p>This handles the case where a cluster member claims entries but crashes
 * or becomes unresponsive before delivering them. The stale-claim sweeper
 * periodically applies this processor to all CLAIMED entries.
 *
 * <p>Returns {@code true} if the entry was released back to PENDING.
 *
 * @author Generated by Claude Code
 * @since 3.1
 */
public class ReleaseClaimEntryProcessor implements EntryProcessor<String, GenericRecord, Boolean>, Serializable {

    private static final long serialVersionUID = 1L;

    private final long staleThresholdMillis;

    /**
     * Creates a new ReleaseClaimEntryProcessor.
     *
     * @param staleTimeoutMs claims older than this (in millis from now) are released
     */
    public ReleaseClaimEntryProcessor(final long staleTimeoutMs) {
        this.staleThresholdMillis = System.currentTimeMillis() - staleTimeoutMs;
    }

    @Override
    public Boolean process(final Map.Entry<String, GenericRecord> entry) {
        GenericRecord record = entry.getValue();
        if (record == null) {
            return false;
        }

        String status = record.getString("status");
        if (!"CLAIMED".equals(status)) {
            return false;
        }

        Long claimedAtMillis = record.getNullableInt64("claimedAt");
        if (claimedAtMillis == null || claimedAtMillis > staleThresholdMillis) {
            return false; // Not stale yet
        }

        // Revert to PENDING and clear claim fields
        GenericRecordBuilder builder = record.newBuilderWithClone();
        builder.setString("status", OutboxEntry.Status.PENDING.name());
        builder.setString("claimantId", null);
        builder.setNullableInt64("claimedAt", null);

        entry.setValue(builder.build());
        return true;
    }
}
