package com.theyawns.framework.config;

import com.hazelcast.config.Config;

/**
 * Callback interface for customizing a Hazelcast embedded {@link Config} before
 * instance creation.
 *
 * <p>Implementations are auto-discovered as Spring beans and applied by service
 * configuration classes. This allows framework-level features (e.g., HD Memory,
 * TPC) to transparently inject configuration into service-owned Hazelcast instances.
 *
 * <p>Usage pattern in service configs:
 * <pre>{@code
 * @Autowired(required = false)
 * private List<HazelcastConfigCustomizer> configCustomizers;
 *
 * @Bean
 * public HazelcastInstance hazelcastInstance() {
 *     Config config = new Config();
 *     // ... service-specific config ...
 *     if (configCustomizers != null) {
 *         configCustomizers.forEach(c -> c.customize(config));
 *     }
 *     return Hazelcast.newHazelcastInstance(config);
 * }
 * }</pre>
 *
 * @author Generated by Claude Code
 * @since 2.0
 * @see HazelcastClientConfigCustomizer
 * @see HazelcastFeatureAutoConfiguration
 */
@FunctionalInterface
public interface HazelcastConfigCustomizer {

    /**
     * Customizes the given Hazelcast Config before instance creation.
     *
     * @param config the Hazelcast Config to customize
     */
    void customize(Config config);
}
