package com.theyawns.framework.dlq;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.RestController;

import java.util.Map;

/**
 * REST controller for managing fault injection state.
 *
 * <p>Provides endpoints to enable and disable fault injection for DLQ demo scenarios.
 * When fault injection is enabled, saga listeners throw exceptions before processing,
 * which triggers the circuit breaker exhaustion path and routes events to the DLQ.
 *
 * <p>Only active when {@code framework.dlq.enabled=true} (same condition as the DLQ itself).
 * Auto-configured by {@link DeadLetterQueueAutoConfiguration}.
 *
 * @author Generated by Claude Code
 * @since 3.0
 */
@RestController
@RequestMapping("/api/admin/fault")
public class FaultInjectionController {

    private static final Logger logger = LoggerFactory.getLogger(FaultInjectionController.class);

    private final FaultInjectionState faultInjectionState;

    /**
     * Creates a new FaultInjectionController.
     *
     * @param faultInjectionState the shared fault injection state
     */
    public FaultInjectionController(final FaultInjectionState faultInjectionState) {
        this.faultInjectionState = faultInjectionState;
    }

    /**
     * Enables or disables fault injection.
     *
     * @param enabled true to enable fault injection
     * @param failureMessage optional custom failure message
     * @return the updated fault injection state
     */
    @PostMapping
    public ResponseEntity<Map<String, Object>> setFaultInjection(
            @RequestParam final boolean enabled,
            @RequestParam(required = false) final String failureMessage) {
        faultInjectionState.setEnabled(enabled);
        if (failureMessage != null) {
            faultInjectionState.setFailureMessage(failureMessage);
        }
        logger.info("Fault injection {}: message='{}'",
                enabled ? "ENABLED" : "DISABLED", faultInjectionState.getFailureMessage());
        return ResponseEntity.ok(currentState());
    }

    /**
     * Returns the current fault injection state.
     *
     * @return the current state
     */
    @GetMapping
    public ResponseEntity<Map<String, Object>> getFaultInjection() {
        return ResponseEntity.ok(currentState());
    }

    private Map<String, Object> currentState() {
        return Map.of(
                "enabled", faultInjectionState.isEnabled(),
                "failureMessage", faultInjectionState.getFailureMessage()
        );
    }
}
