package com.theyawns.ecommerce.order.controller;

import com.theyawns.framework.saga.SagaStateStore;
import com.theyawns.framework.saga.SagaStatus;
import io.micrometer.core.instrument.Counter;
import io.micrometer.core.instrument.MeterRegistry;
import io.swagger.v3.oas.annotations.Operation;
import io.swagger.v3.oas.annotations.responses.ApiResponse;
import io.swagger.v3.oas.annotations.tags.Tag;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

import java.util.LinkedHashMap;
import java.util.Map;

/**
 * REST controller for aggregated system metrics.
 *
 * <p>Provides a clean JSON summary of saga and event metrics, suitable
 * for the MCP server's {@code getMetrics} tool and monitoring dashboards.
 *
 * <p>Endpoints:
 * <ul>
 *   <li>GET /api/metrics/summary - Aggregated metrics summary</li>
 * </ul>
 *
 * @author Generated by Claude Code
 * @since 2.0
 */
@RestController
@RequestMapping("/api/metrics")
@Tag(name = "Metrics", description = "APIs for system metrics")
public class MetricsController {

    private static final Logger logger = LoggerFactory.getLogger(MetricsController.class);

    private final SagaStateStore sagaStateStore;
    private final MeterRegistry meterRegistry;

    /**
     * Creates a new MetricsController.
     *
     * @param sagaStateStore the saga state store for saga counts
     * @param meterRegistry the Micrometer registry for event metrics
     */
    public MetricsController(SagaStateStore sagaStateStore, MeterRegistry meterRegistry) {
        this.sagaStateStore = sagaStateStore;
        this.meterRegistry = meterRegistry;
    }

    /**
     * Returns an aggregated metrics summary.
     *
     * @return JSON object with saga and event metrics
     */
    @GetMapping("/summary")
    @Operation(summary = "Get metrics summary",
            description = "Returns aggregated saga and event metrics for the system")
    @ApiResponse(responseCode = "200", description = "Metrics retrieved")
    public ResponseEntity<Map<String, Object>> getMetricsSummary() {
        logger.debug("REST: Getting metrics summary");

        Map<String, Object> summary = new LinkedHashMap<>();

        // Saga metrics from SagaStateStore
        Map<String, Object> sagaMetrics = new LinkedHashMap<>();
        sagaMetrics.put("total", sagaStateStore.count());
        sagaMetrics.put("started", sagaStateStore.countByStatus(SagaStatus.STARTED));
        sagaMetrics.put("inProgress", sagaStateStore.countByStatus(SagaStatus.IN_PROGRESS));
        sagaMetrics.put("completed", sagaStateStore.countByStatus(SagaStatus.COMPLETED));
        sagaMetrics.put("compensating", sagaStateStore.countByStatus(SagaStatus.COMPENSATING));
        sagaMetrics.put("compensated", sagaStateStore.countByStatus(SagaStatus.COMPENSATED));
        sagaMetrics.put("failed", sagaStateStore.countByStatus(SagaStatus.FAILED));
        sagaMetrics.put("timedOut", sagaStateStore.countByStatus(SagaStatus.TIMED_OUT));
        summary.put("sagas", sagaMetrics);

        // Event metrics from Micrometer
        Map<String, Object> eventMetrics = new LinkedHashMap<>();
        eventMetrics.put("eventsSubmitted", getCounterValue("events.submitted"));
        eventMetrics.put("eventsProcessed", getCounterValue("events.processed"));
        eventMetrics.put("eventsFailed", getCounterValue("events.failed"));
        summary.put("events", eventMetrics);

        // Saga gauge metrics from Micrometer (registered by SagaMetrics)
        Map<String, Object> gauges = new LinkedHashMap<>();
        gauges.put("activeSagas", getGaugeValue("sagas.active.count"));
        gauges.put("compensatingSagas", getGaugeValue("sagas.compensating.count"));
        summary.put("gauges", gauges);

        return ResponseEntity.ok(summary);
    }

    /**
     * Gets the current value of a counter from the meter registry.
     *
     * @param name the counter name
     * @return the counter value, or 0.0 if not found
     */
    private double getCounterValue(String name) {
        Counter counter = meterRegistry.find(name).counter();
        return counter != null ? counter.count() : 0.0;
    }

    /**
     * Gets the current value of a gauge from the meter registry.
     *
     * @param name the gauge name
     * @return the gauge value, or 0.0 if not found
     */
    private double getGaugeValue(String name) {
        io.micrometer.core.instrument.Gauge gauge = meterRegistry.find(name).gauge();
        return gauge != null ? gauge.value() : 0.0;
    }
}
