    @Test
    @DisplayName("should persist and load a single DLQ entry")
    void shouldPersistAndLoadSingleEntry() {
        // Arrange
        PersistableDeadLetterEntry entry = new PersistableDeadLetterEntry(
                "dlq-001", "evt-001", "OrderCreated", "OrderCreated",
                "{\"_schemaType\":\"OrderCreated\",\"_fieldKinds\":{},\"_fields\":{}}",
                "Connection refused", 1700000000000L,
                "inventory-service", "saga-001", "corr-001", 0, "PENDING");

        // Act
        persistence.persist(entry);
        Optional<PersistableDeadLetterEntry> loaded = persistence.load("dlq-001");

        // Assert
        assertTrue(loaded.isPresent());
        PersistableDeadLetterEntry result = loaded.get();
        assertEquals("dlq-001", result.dlqEntryId());
        assertEquals("evt-001", result.originalEventId());
        assertEquals("OrderCreated", result.eventType());
        assertEquals("OrderCreated", result.topicName());
        assertTrue(result.eventData().contains("OrderCreated"));
        assertEquals("Connection refused", result.failureReason());
        assertEquals(1700000000000L, result.failureTimestampMillis());
        assertEquals("inventory-service", result.sourceService());
        assertEquals("saga-001", result.sagaId());
        assertEquals("corr-001", result.correlationId());
        assertEquals(0, result.replayCount());
        assertEquals("PENDING", result.status());
    }

    @Test
    @DisplayName("should upsert DLQ entry (update replay count and status)")
    void shouldUpsertEntry() {
        // Arrange
        PersistableDeadLetterEntry initial = new PersistableDeadLetterEntry(
                "dlq-001", "evt-001", "OrderCreated", "OrderCreated",
                null, "Error", 1700000000000L,
                "test-service", null, null, 0, "PENDING");
        persistence.persist(initial);

        // Act — update same key with REPLAYED status
        PersistableDeadLetterEntry replayed = new PersistableDeadLetterEntry(
                "dlq-001", "evt-001", "OrderCreated", "OrderCreated",
                null, "Error", 1700000000000L,
                "test-service", null, null, 1, "REPLAYED");
        persistence.persist(replayed);

        // Assert
        Optional<PersistableDeadLetterEntry> loaded = persistence.load("dlq-001");
        assertTrue(loaded.isPresent());
        assertEquals("REPLAYED", loaded.get().status());
        assertEquals(1, loaded.get().replayCount());
    }

    @Test
    @DisplayName("should persist a batch of DLQ entries")
    void shouldPersistBatch() {
        // Arrange
        List<PersistableDeadLetterEntry> entries = List.of(
                new PersistableDeadLetterEntry("dlq-001", "evt-001", "OrderCreated", "OrderCreated",
                        null, "Error1", 100L, "svc1", null, null, 0, "PENDING"),
                new PersistableDeadLetterEntry("dlq-002", "evt-002", "StockReserved", "StockReserved",
                        null, "Error2", 200L, "svc2", null, null, 0, "PENDING"),
                new PersistableDeadLetterEntry("dlq-003", "evt-003", "PaymentFailed", "PaymentFailed",
                        null, "Error3", 300L, "svc3", null, null, 1, "DISCARDED")
        );

        // Act
        persistence.persistBatch(entries);

        // Assert
        assertEquals(3, repository.count());
        assertTrue(persistence.load("dlq-001").isPresent());
        assertTrue(persistence.load("dlq-002").isPresent());
        PersistableDeadLetterEntry discarded = persistence.load("dlq-003").orElseThrow();
        assertEquals("DISCARDED", discarded.status());
        assertEquals(1, discarded.replayCount());
    }

    @Test
    @DisplayName("should load only PENDING keys")
    void shouldLoadOnlyPendingKeys() {
        // Arrange
        persistence.persist(new PersistableDeadLetterEntry("dlq-001", "evt-001", "Evt", "Topic",
                null, "err", 100L, "svc", null, null, 0, "PENDING"));
        persistence.persist(new PersistableDeadLetterEntry("dlq-002", "evt-002", "Evt", "Topic",
                null, "err", 200L, "svc", null, null, 1, "REPLAYED"));
        persistence.persist(new PersistableDeadLetterEntry("dlq-003", "evt-003", "Evt", "Topic",
                null, "err", 300L, "svc", null, null, 0, "PENDING"));
        persistence.persist(new PersistableDeadLetterEntry("dlq-004", "evt-004", "Evt", "Topic",
                null, "err", 400L, "svc", null, null, 0, "DISCARDED"));

        // Act
        List<String> keys = StreamSupport.stream(
                persistence.loadPendingKeys().spliterator(), false).toList();

        // Assert — only PENDING entries returned
        assertEquals(2, keys.size());
        assertTrue(keys.contains("dlq-001"));
        assertTrue(keys.contains("dlq-003"));
        assertFalse(keys.contains("dlq-002")); // REPLAYED excluded
        assertFalse(keys.contains("dlq-004")); // DISCARDED excluded
    }

