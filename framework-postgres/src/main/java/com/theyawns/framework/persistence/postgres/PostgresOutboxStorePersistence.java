package com.theyawns.framework.persistence.postgres;

import com.theyawns.framework.persistence.OutboxStorePersistence;
import com.theyawns.framework.persistence.PersistableOutboxEntry;
import com.theyawns.framework.persistence.postgres.entity.OutboxEntity;
import com.theyawns.framework.persistence.postgres.repository.OutboxRepository;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.jdbc.core.JdbcTemplate;
import org.springframework.transaction.annotation.Transactional;

import java.util.List;
import java.util.Optional;

/**
 * PostgreSQL implementation of {@link OutboxStorePersistence}.
 *
 * <p>Uses JDBC upsert semantics ({@code INSERT ... ON CONFLICT DO UPDATE}) since
 * outbox entries transition through status changes (PENDING → CLAIMED → DELIVERED)
 * and only the latest state matters.
 *
 * @author Generated by Claude Code
 * @since 3.0
 */
public class PostgresOutboxStorePersistence implements OutboxStorePersistence {

    private static final Logger logger = LoggerFactory.getLogger(PostgresOutboxStorePersistence.class);

    private static final String UPSERT_SQL =
            "INSERT INTO domain_outbox (event_id, event_type, event_data, retry_count, status, " +
            "created_at_millis, last_attempt_at_millis, failure_reason, claimant_id, claimed_at_millis, updated_at) " +
            "VALUES (?, ?, ?::jsonb, ?, ?, ?, ?, ?, ?, ?, NOW()) " +
            "ON CONFLICT (event_id) DO UPDATE SET " +
            "event_type = EXCLUDED.event_type, " +
            "event_data = EXCLUDED.event_data, " +
            "retry_count = EXCLUDED.retry_count, " +
            "status = EXCLUDED.status, " +
            "last_attempt_at_millis = EXCLUDED.last_attempt_at_millis, " +
            "failure_reason = EXCLUDED.failure_reason, " +
            "claimant_id = EXCLUDED.claimant_id, " +
            "claimed_at_millis = EXCLUDED.claimed_at_millis, " +
            "updated_at = NOW()";

    private final OutboxRepository repository;
    private final JdbcTemplate jdbcTemplate;

    /**
     * Creates a new PostgresOutboxStorePersistence.
     *
     * @param repository the JPA repository
     * @param jdbcTemplate the JDBC template for batch operations
     */
    public PostgresOutboxStorePersistence(OutboxRepository repository, JdbcTemplate jdbcTemplate) {
        this.repository = repository;
        this.jdbcTemplate = jdbcTemplate;
    }

    @Override
    @Transactional
    public void persist(PersistableOutboxEntry entry) {
        jdbcTemplate.update(UPSERT_SQL,
                entry.eventId(),
                entry.eventType(),
                entry.eventData(),
                entry.retryCount(),
                entry.status(),
                entry.createdAtMillis(),
                entry.lastAttemptAtMillis(),
                entry.failureReason(),
                entry.claimantId(),
                entry.claimedAtMillis());
        logger.debug("Persisted outbox entry: eventId={}, status={}", entry.eventId(), entry.status());
    }

    @Override
    @Transactional
    public void persistBatch(List<PersistableOutboxEntry> entries) {
        if (entries.isEmpty()) {
            return;
        }

        jdbcTemplate.batchUpdate(UPSERT_SQL, entries, entries.size(),
                (ps, entry) -> {
                    ps.setString(1, entry.eventId());
                    ps.setString(2, entry.eventType());
                    ps.setString(3, entry.eventData());
                    ps.setInt(4, entry.retryCount());
                    ps.setString(5, entry.status());
                    ps.setLong(6, entry.createdAtMillis());
                    if (entry.lastAttemptAtMillis() != null) {
                        ps.setLong(7, entry.lastAttemptAtMillis());
                    } else {
                        ps.setNull(7, java.sql.Types.BIGINT);
                    }
                    ps.setString(8, entry.failureReason());
                    ps.setString(9, entry.claimantId());
                    if (entry.claimedAtMillis() != null) {
                        ps.setLong(10, entry.claimedAtMillis());
                    } else {
                        ps.setNull(10, java.sql.Types.BIGINT);
                    }
                });
        logger.debug("Batch persisted {} outbox entries", entries.size());
    }

    @Override
    @Transactional(readOnly = true)
    public Optional<PersistableOutboxEntry> load(String eventId) {
        return repository.findByEventId(eventId)
                .map(this::fromEntity);
    }

    @Override
    @Transactional(readOnly = true)
    public Iterable<String> loadNonDeliveredKeys() {
        return repository.findAllNonDeliveredEventIds();
    }

    @Override
    @Transactional
    public void delete(String eventId) {
        repository.deleteById(eventId);
        logger.debug("Deleted outbox entry: eventId={}", eventId);
    }

    @Override
    public boolean isAvailable() {
        try {
            jdbcTemplate.queryForObject("SELECT 1", Integer.class);
            return true;
        } catch (Exception e) {
            logger.warn("PostgreSQL persistence unavailable: {}", e.getMessage());
            return false;
        }
    }

    private PersistableOutboxEntry fromEntity(OutboxEntity entity) {
        return new PersistableOutboxEntry(
                entity.getEventId(),
                entity.getEventType(),
                entity.getEventData(),
                entity.getRetryCount(),
                entity.getStatus(),
                entity.getCreatedAtMillis(),
                entity.getLastAttemptAtMillis(),
                entity.getFailureReason(),
                entity.getClaimantId(),
                entity.getClaimedAtMillis()
        );
    }
}
