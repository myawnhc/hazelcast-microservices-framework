package com.theyawns.ecommerce.common.events;

import com.hazelcast.nio.serialization.genericrecord.GenericRecord;
import com.hazelcast.nio.serialization.genericrecord.GenericRecordBuilder;
import com.theyawns.framework.event.DomainEvent;
import com.theyawns.framework.saga.SagaCompensationConfig;
import com.theyawns.framework.saga.SagaEvent;
import com.theyawns.ecommerce.common.domain.Payment;

import java.time.Instant;

/**
 * Event representing the refund of a previously captured payment.
 *
 * <p>A payment is refunded when:
 * <ul>
 *   <li>The order fulfillment saga is compensating</li>
 *   <li>A customer requests a refund</li>
 *   <li>An administrative action triggers a refund</li>
 * </ul>
 *
 * <p>This is the compensating event for {@link PaymentProcessedEvent} in the
 * Order Fulfillment saga. It undoes step 2 (payment capture).
 *
 * @author Generated by Claude Code
 * @since 2.0
 * @see PaymentProcessedEvent
 * @see SagaEvent
 */
public class PaymentRefundedEvent extends DomainEvent<Payment, String>
        implements SagaEvent<Payment, String> {

    public static final String SCHEMA_NAME = "PaymentRefundedEvent";
    public static final String EVENT_TYPE = "PaymentRefunded";

    private String orderId;
    private String amount;
    private String reason;
    private String refundTransactionId;

    /**
     * Default constructor for serialization.
     */
    public PaymentRefundedEvent() {
        super();
        this.eventType = EVENT_TYPE;
    }

    /**
     * Creates a new PaymentRefundedEvent.
     *
     * @param paymentId the payment ID being refunded
     * @param orderId the associated order ID
     * @param amount the refund amount
     * @param reason the reason for the refund
     * @param refundTransactionId the external refund transaction ID
     */
    public PaymentRefundedEvent(String paymentId, String orderId,
                                 String amount, String reason,
                                 String refundTransactionId) {
        super(paymentId);
        this.eventType = EVENT_TYPE;
        this.orderId = orderId;
        this.amount = amount;
        this.reason = reason;
        this.refundTransactionId = refundTransactionId;
    }

    /**
     * Creates a PaymentRefundedEvent from a GenericRecord.
     *
     * @param record the GenericRecord to hydrate from
     * @return the hydrated event
     */
    public static PaymentRefundedEvent fromGenericRecord(GenericRecord record) {
        if (record == null) {
            return null;
        }
        PaymentRefundedEvent event = new PaymentRefundedEvent();
        event.eventId = record.getString("eventId");
        event.eventType = record.getString("eventType");
        event.eventVersion = record.getString("eventVersion");
        event.source = record.getString("source");
        event.key = record.getString("key");
        event.correlationId = record.getString("correlationId");

        Long timestampMs = record.getInt64("timestamp");
        event.timestamp = timestampMs != null && timestampMs != 0 ? Instant.ofEpochMilli(timestampMs) : null;

        event.orderId = record.getString("orderId");
        event.amount = record.getString("amount");
        event.reason = record.getString("reason");
        event.refundTransactionId = record.getString("refundTransactionId");

        // Saga fields
        event.sagaId = record.getString("sagaId");
        event.sagaType = record.getString("sagaType");
        event.stepNumber = record.getInt32("stepNumber");
        event.isCompensating = record.getBoolean("isCompensating");

        return event;
    }

    @Override
    public GenericRecord toGenericRecord() {
        return GenericRecordBuilder.compact(SCHEMA_NAME)
                .setString("eventId", eventId)
                .setString("eventType", eventType)
                .setString("eventVersion", eventVersion)
                .setString("source", source)
                .setInt64("timestamp", timestamp != null ? timestamp.toEpochMilli() : 0)
                .setString("key", key)
                .setString("correlationId", correlationId)
                .setString("sagaId", sagaId)
                .setString("sagaType", sagaType)
                .setInt32("stepNumber", stepNumber != null ? stepNumber : 0)
                .setBoolean("isCompensating", isCompensating != null && isCompensating)
                .setString("orderId", orderId)
                .setString("amount", amount)
                .setString("reason", reason)
                .setString("refundTransactionId", refundTransactionId)
                .build();
    }

    @Override
    public GenericRecord apply(GenericRecord currentState) {
        if (currentState == null) {
            throw new IllegalStateException("Cannot refund a non-existent payment");
        }

        Instant now = Instant.now();
        return GenericRecordBuilder.compact(Payment.SCHEMA_NAME)
                .setString("paymentId", currentState.getString("paymentId"))
                .setString("orderId", currentState.getString("orderId"))
                .setString("customerId", currentState.getString("customerId"))
                .setString("amount", currentState.getString("amount"))
                .setString("currency", currentState.getString("currency"))
                .setString("method", currentState.getString("method"))
                .setString("status", Payment.PaymentStatus.REFUNDED.name())
                .setString("transactionId", currentState.getString("transactionId"))
                .setInt64("processedAt", currentState.getInt64("processedAt"))
                .setString("failureReason", reason)
                .setInt64("createdAt", currentState.getInt64("createdAt"))
                .setInt64("updatedAt", now.toEpochMilli())
                .build();
    }

    @Override
    public String getSchemaName() {
        return SCHEMA_NAME;
    }

    // Getters and Setters

    public String getOrderId() {
        return orderId;
    }

    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public String getRefundTransactionId() {
        return refundTransactionId;
    }

    public void setRefundTransactionId(String refundTransactionId) {
        this.refundTransactionId = refundTransactionId;
    }

    @Override
    public String toString() {
        return "PaymentRefundedEvent{" +
                "eventId='" + eventId + '\'' +
                ", paymentId='" + key + '\'' +
                ", orderId='" + orderId + '\'' +
                ", amount='" + amount + '\'' +
                ", reason='" + reason + '\'' +
                '}';
    }

    // SagaEvent interface implementation

    /**
     * Returns step 2 - compensates the PaymentProcessed step.
     *
     * @return 2 (compensates third step)
     */
    @Override
    public int getSagaStepNumber() {
        return SagaCompensationConfig.STEP_PAYMENT_PROCESSED;
    }

    /**
     * Compensation events don't have their own compensating event.
     *
     * @return null
     */
    @Override
    public String getCompensatingEventType() {
        return null;
    }

    /**
     * PaymentRefundedEvent is a compensation event that undoes PaymentProcessed.
     *
     * @return true
     */
    @Override
    public boolean isCompensatingEvent() {
        return true;
    }

    /**
     * Returns the saga type name for Order Fulfillment.
     *
     * @return "OrderFulfillment"
     */
    @Override
    public String getSagaTypeName() {
        return SagaCompensationConfig.ORDER_FULFILLMENT_SAGA;
    }
}
