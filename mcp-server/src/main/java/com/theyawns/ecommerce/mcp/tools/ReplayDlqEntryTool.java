package com.theyawns.ecommerce.mcp.tools;

import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.ObjectMapper;
import com.fasterxml.jackson.databind.SerializationFeature;
import com.theyawns.ecommerce.mcp.client.ServiceClientOperations;
import com.theyawns.ecommerce.mcp.security.ToolAuthorizer;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.ai.tool.annotation.Tool;
import org.springframework.ai.tool.annotation.ToolParam;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.util.Map;

/**
 * MCP tool for replaying a DLQ entry.
 *
 * <p>Replays a dead letter queue entry by re-publishing the original event
 * to its ITopic. Use after investigating the failure cause and ensuring
 * the root issue is resolved.
 *
 * @author Generated by Claude Code
 * @since 3.0
 */
@Service
public class ReplayDlqEntryTool {

    private static final Logger logger = LoggerFactory.getLogger(ReplayDlqEntryTool.class);

    private final ServiceClientOperations serviceClient;
    private final ObjectMapper objectMapper;
    private ToolAuthorizer toolAuthorizer;

    /**
     * Creates a new ReplayDlqEntryTool.
     *
     * @param serviceClient the REST client for service communication
     */
    public ReplayDlqEntryTool(ServiceClientOperations serviceClient) {
        this.serviceClient = serviceClient;
        this.objectMapper = new ObjectMapper().enable(SerializationFeature.INDENT_OUTPUT);
    }

    /**
     * Sets the tool authorizer for role-based access control.
     *
     * @param toolAuthorizer the authorizer (injected only when security is enabled)
     */
    @Autowired(required = false)
    public void setToolAuthorizer(final ToolAuthorizer toolAuthorizer) {
        this.toolAuthorizer = toolAuthorizer;
    }

    /**
     * Replays a DLQ entry.
     *
     * @param dlqEntryId the DLQ entry ID to replay
     * @param serviceName optional service name to target (null defaults to order-service)
     * @return JSON string with the replay result
     */
    @Tool(description = "Replay a dead letter queue entry. Re-publishes the original event "
            + "to its ITopic for reprocessing. IMPORTANT: Before replaying, use inspectDlqEntry "
            + "to understand the failure cause and ensure the root issue is resolved. "
            + "Replaying without fixing the cause will send the event back to the DLQ.")
    public String replayDlqEntry(
            @ToolParam(description = "The DLQ entry ID to replay")
            String dlqEntryId,
            @ToolParam(description = "Service to target (optional, defaults to order)")
            String serviceName) {

        if (toolAuthorizer != null) {
            String denied = toolAuthorizer.checkAccess("replayDlqEntry");
            if (denied != null) {
                return denied;
            }
        }

        logger.info("MCP replayDlqEntry: id={}, serviceName={}", dlqEntryId, serviceName);

        try {
            Map<String, Object> result = serviceClient.replayDlqEntry(serviceName, dlqEntryId);
            return toJson(result);
        } catch (Exception e) {
            logger.error("Failed to replay DLQ entry: {}", dlqEntryId, e);
            return toJson(Map.of("error", "Failed to replay DLQ entry: " + e.getMessage()));
        }
    }

    private String toJson(Object value) {
        try {
            return objectMapper.writeValueAsString(value);
        } catch (JsonProcessingException e) {
            logger.error("Failed to serialize result", e);
            return "{\"error\": \"Failed to serialize result\"}";
        }
    }
}
