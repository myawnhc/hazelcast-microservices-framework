package com.theyawns.ecommerce.mcp.tools;

import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.ObjectMapper;
import com.fasterxml.jackson.databind.SerializationFeature;
import com.theyawns.ecommerce.mcp.client.ServiceClientOperations;
import com.theyawns.ecommerce.mcp.security.ToolAuthorizer;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.ai.tool.annotation.Tool;
import org.springframework.ai.tool.annotation.ToolParam;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.util.LinkedHashMap;
import java.util.List;
import java.util.Map;

/**
 * MCP tool for listing dead letter queue entries.
 *
 * <p>Lists DLQ entries from a service, optionally filtered by service name.
 * Also returns the count of pending entries for quick triage.
 *
 * @author Generated by Claude Code
 * @since 3.0
 */
@Service
public class ListDlqEntriesTool {

    private static final Logger logger = LoggerFactory.getLogger(ListDlqEntriesTool.class);

    private final ServiceClientOperations serviceClient;
    private final ObjectMapper objectMapper;
    private ToolAuthorizer toolAuthorizer;

    /**
     * Creates a new ListDlqEntriesTool.
     *
     * @param serviceClient the REST client for service communication
     */
    public ListDlqEntriesTool(ServiceClientOperations serviceClient) {
        this.serviceClient = serviceClient;
        this.objectMapper = new ObjectMapper().enable(SerializationFeature.INDENT_OUTPUT);
    }

    /**
     * Sets the tool authorizer for role-based access control.
     *
     * @param toolAuthorizer the authorizer (injected only when security is enabled)
     */
    @Autowired(required = false)
    public void setToolAuthorizer(final ToolAuthorizer toolAuthorizer) {
        this.toolAuthorizer = toolAuthorizer;
    }

    /**
     * Lists DLQ entries with optional service name filter.
     *
     * @param serviceName optional service name (null defaults to order-service)
     * @param limit maximum entries to return (default 20)
     * @return JSON string with DLQ entries and pending count
     */
    @Tool(description = "List dead letter queue (DLQ) entries. Shows failed events that "
            + "need investigation or replay. Includes pending count for triage.")
    public String listDlqEntries(
            @ToolParam(description = "Service to query: account, inventory, order, or payment. "
                    + "All services see the same DLQ (shared cluster). Defaults to order.")
            String serviceName,
            @ToolParam(description = "Maximum entries to return (default 20)")
            Integer limit) {

        if (toolAuthorizer != null) {
            String denied = toolAuthorizer.checkAccess("listDlqEntries");
            if (denied != null) {
                return denied;
            }
        }

        int effectiveLimit = (limit != null && limit > 0) ? limit : 20;
        logger.info("MCP listDlqEntries: serviceName={}, limit={}", serviceName, effectiveLimit);

        try {
            Map<String, Object> result = new LinkedHashMap<>();

            List<Map<String, Object>> entries = serviceClient.listDlqEntries(serviceName, effectiveLimit);
            result.put("entries", entries);
            result.put("count", entries.size());

            Map<String, Object> pendingCount = serviceClient.getDlqCount(serviceName);
            result.put("pendingCount", pendingCount);

            return toJson(result);
        } catch (Exception e) {
            logger.error("Failed to list DLQ entries", e);
            return toJson(Map.of("error", "Failed to list DLQ entries: " + e.getMessage()));
        }
    }

    private String toJson(Object value) {
        try {
            return objectMapper.writeValueAsString(value);
        } catch (JsonProcessingException e) {
            logger.error("Failed to serialize result", e);
            return "{\"error\": \"Failed to serialize result\"}";
        }
    }
}
